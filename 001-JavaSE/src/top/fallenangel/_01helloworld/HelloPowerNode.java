public class HelloPowerNode {
    /**
     * main方法是一切程序的入口，没有main方法则程序无法运行。学习main和输出语句的用法。
     *
     * @param args main方法接收的参数
     *
     * @author 杜海蛟
     * @since 2020-7-14 11:10
     */
    public static void main(String[] args) {
        /*
         * 学习输出语句的用法
         * println是换行输出
         * print是不换行输出
         */
        // 输出“Hello PowerNode”
        System.out.println("Hello PowerNode!");
    }
}

